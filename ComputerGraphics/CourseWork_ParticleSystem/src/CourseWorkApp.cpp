//////////////////////////////////////////////////////////////////////////
#include "CourseWorkApp.h"
//////////////////////////////////////////////////////////////////////////
CourseWorkApp::CourseWorkApp(
	HINSTANCE hInstance,
	HINSTANCE hPrevInstance,
	LPSTR lpCmdLine,
	int  nCmdShow): App(
		hInstance,
		hPrevInstance,
		lpCmdLine,
		nCmdShow)
	
{
	EnableOnIdle(true);
}
//////////////////////////////////////////////////////////////////////////
CourseWorkApp::~CourseWorkApp(void)
{
	SAFE_DELETE(m_pAppWnd);
}
//////////////////////////////////////////////////////////////////////////
BOOL CourseWorkApp::OnInitApp()
{
	m_pAppWnd = new AppWnd(*this);

	m_pAppWnd->Create(GetModuleHandle(NULL),NULL);
	m_pAppWnd->ShowWindow(SW_SHOW);
	m_pAppWnd->UpdateWindow();

	return true;
}
//////////////////////////////////////////////////////////////////////////
void CourseWorkApp::OnIdle()
{
	m_pAppWnd->Update();
	m_pAppWnd->Render();
} 
